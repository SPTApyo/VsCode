FROM ATHLETE A1
WHERE A1.PAYSN <> 'MONACO'
  AND A1.PAYSN <> 'GUADELOUPE'
  AND A1.PAYSN <> 'REUNION'
  AND A1.PAYSN <> 'MAYOTTE'
  AND A1.PAYSN <> 'MARTINIQUE'
  AND A1.PAYSN <> 'FRANCE'
  AND A1.CIO = 'FRA';


SELECT DISTINCT A.NDA, A.NCOMPLET, A.CIO
FROM ATHLETE A, PRATIQUER P1, PRATIQUER P2, DISCIPLINE D1, DISCIPLINE D2
WHERE D1.CDS <> D2.CDS
  AND P2.NDD = D2.NDD
  AND P1.NDD = D1.NDD
  AND A.NDA = P2.NDA
  AND A.NDA = P1.NDA;
